def _strip_bullet_prefix(text: str) -> str:
    """去掉开头可能重复的 bullet 标记（- / * / •），避免模板拼接出现 '- - xxx'。"""
    return re.sub(r"^\s*[-*•]\s*", "", text or "").strip()

SYSTEM_ZH = "你是 DailyDawn 主笔，技术派独立开发者观察员。严格输出 JSON，不要解释。"
SYSTEM_EN = "You are the DailyDawn editor-in-chief. Output strict JSON only; no commentary."

